            function initVideo() {
                var currentState = ($(window).width() < $(window).height()) ? 'portrait' : 'landscape';

                if ((videoInitialized == true) && (videoLastState == currentState)) {
                    return;
                }


                // console.log($('#hiddenAssetUrl').val());

                source.setAttribute('src', scope.vm.assetUrl + '/' + currentState + '.mp4');
                source.setAttribute('type', 'video/mp4');

                if (videoInitialized === false)
                    video.appendChild(source);
                else
                    video.load();
                video.play();

                videoInitialized = true;
                videoLastState = currentState;

                console.log('initialized video to state ' + currentState);
            }

            handleScope();

